import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import tensorflow as tf

tf.__version__

"""# Part 1 - Data Preprocessing

Importing The Dataset
"""

df = pd.read_csv('Churn_Modelling.csv')
df.columns

X = df.iloc[:, 3:-1].values
y = df.iloc[:, -1].values

print(X)

print(y)

"""Encoding Categorical Data

Label encoding the "Gender" columns
"""

from sklearn.preprocessing import LabelEncoder
le = LabelEncoder()
X[:, 2] = le.fit_transform(X[:, 2])

print(X)

"""One Hot ENcoding of the "Geography" Column"""

from sklearn.compose import ColumnTransformer
from sklearn.preprocessing import OneHotEncoder
ct = ColumnTransformer(transformers = [('encoder', OneHotEncoder(), [1])], remainder = 'passthrough')
X = np.array(ct.fit_transform(X))

print(X)

"""Spliting the Dataset into training and test set"""

from sklearn.model_selection import train_test_split
X_train, X_test, y_train, y_test = train_test_split(X, y, test_size = 0.2, random_state = 0)

"""Feature Scaling - this is fundamental whenever you are working on deep learning."""

from sklearn.preprocessing import StandardScaler
sc = StandardScaler()
X_train = sc.fit_transform(X_train)
X_test = sc.fit_transform(X_test)

"""# Part 2 - Building The ANN

Initializing The ANN
"""

ann = tf.keras.models.Sequential()

"""Adding the First Input Layer and The First Hidden Layer"""

ann.add(tf.keras.layers.Dense(units = 6, activation = 'relu'))

"""Adding the Second Input layer"""

ann.add(tf.keras.layers.Dense(units=6, activation='relu'))

"""Adding the Output Layer"""

ann.add(tf.keras.layers.Dense(units=1, activation='sigmoid'))

"""# Part 3 - Training The ANN

Compiling The ANN
"""

ann.compile(optimizer='adam', loss='binary_crossentropy', metrics=['accuracy'])

"""Training The ANN on The Training Set"""

ann.fit(X_train, y_train, batch_size = 32, epochs = 100)

"""# Part 4 - Making The Predictionds & Evaluating The Model

Predicting the result of a single observation:
Using our ANN model to predict if the customer with the following informations will leave the bank:-

Geography: France,

Credit Score: 600,

Gender: Male,

Age: 40 years old,

Tenure: 3 years,

Balance: $ 60000,

Number of Products: 2,

Does this customer have a credit card ? Yes,

Is this customer an Active Member: Yes,

Estimated Salary: $ 50000,

So, should we say goodbye to that customer?
"""

print(ann.predict(sc.transform([[1,0,0, 600, 1, 40, 3, 60000, 2, 1, 1, 50000]])) > 0.5)

"""Therefore, our ANN model predicts that this customer stays in the bank!

**Important note 1**: The values of the features were all input in a double pair of square brackets. That's because the "predict" method always expects a 2D array as the format of its inputs. And putting our values into a double pair of square brackets makes the input exactly a 2D array.

**Important note 2**: The "France" country was not input as a string in the last column but as "1, 0, 0" in the first three columns.
That's because of course the predict method expects the one-hot-encoded values of the state, and as we see in the first row of the matrix of features X, "France" was encoded as "', 0, 0". And be careful to include these values in the first three columns, because the dummy variables are always created in the first columns.

Predicting The Test Results
"""

y_pred = ann.predict(X_test)
y_pred = (y_pred > 0.5)
print(np.concatenate((y_pred.reshape(len(y_pred),1), y_test. reshape (len (y_test),1)),1))

"""Making The Confusion Matrix"""

from sklearn. metrics import confusion_matrix, accuracy_score
cm = confusion_matrix(y_test, y_pred)
print(cm)
accuracy_score(y_test, y_pred)

"""The accuracy score is 86.25 %
As per the confusion matrix, we have 1536 correct predictions that the customer stays in the bank. We have 189 correct predictions that the customer leaves the bank.
Also, we have 75 incorect predictions that the customer leaves the bank and 216 incorrect predictions that the customer stays in the bank.
"""